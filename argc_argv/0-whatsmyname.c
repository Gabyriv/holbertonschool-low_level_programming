#include <stdio.h>
#include <stdlib.h>

/**
 * main - Prints its name, followed by a new line.
 * @argc: The number of arguments supplied to the program.
 * @argv: An array of pointer to the argument.
 * Return: Always 0.
 */

int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
